let generateBtn = document.getElementById("generatebtn");
let showResults1 = document.getElementById("displaypass1");
let showResults2 = document.getElementById("displaypass2");

generateBtn.addEventListener("click", function () {
  function generatePassword() {
    let showResults1 = "";
    let showResults2 = "";
    for (let i = 0; i < 15; i++) {
      showResults1 += characters[Math.floor(Math.random() * characters.length)];
      showResults2 += characters[Math.floor(Math.random() * characters.length)];
    }
    return showResults1;
    return showResults2;
  }

  showResults1.textContent = generatePassword();
  showResults2.textContent = generatePassword();
});
